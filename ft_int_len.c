#include "libft.h"

int		ft_int_len(int nb)
{
	int		len;

	if (!nb)
		return (0);
	len = 0;
	if (nb < 0)
		len++;
	while (nb)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
